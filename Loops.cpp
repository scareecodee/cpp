/*
Types of Loops in C++
There are three main types of loops:

1.for loop – Best for known repetitions
for (initialization; condition;updation) {
    // Code to repeat
}

2.while loop – Best when condition is checked before each iteration
while (condition) {
    // Code to repeat
}

3.do...while loop – Best when at least one iteration is required
do {
    // Code to repeat
} while (condition);


IMPORTANT
 for(;;){   --->---> infinite for loop
}

*/